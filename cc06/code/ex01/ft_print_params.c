#include <unistd.h>

int	main(int argc, char **argv)
{
	int it;
	int jt;

	jt = 1;
	while (jt < argc)
	{
		it = 0;
		while (argv[jt][it])
		{
			write(1, &argv[jt][it], 1);
			it++;
		}
		write(1, "\n", 1);
		jt++;
	}
}