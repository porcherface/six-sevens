#include <unistd.h>

int	main(int argc, char **argv)
{
	int it;
 
	it = 0;
	while (argv[0][it])
	{
		write(1, &argv[0][it], 1);
		it++;
	}
	write(1, "\n", 1);
	return (argc);
}