char *ft_strcpy(char *dest, char *src)
{
	int it;

	it = 0;
	while(src[it] != '\0')
	{
		dest[it] = src[it];
	}
	if (src[it + 1] == '\0')
		dest[it + 1] = src[it + 1];
	return dest;
}
