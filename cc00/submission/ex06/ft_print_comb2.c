#include <unistd.h>

void	ft_print_comb2(void)
{
	int		c1;
	int		c2;
	char	d;

	c1 = 0;
	while (c1 < 98)
	{
		c2 = c1++ + 1;
		while (c2 < 100)
		{
			d = (c1 - 1 - ((c1 - 1) % 10 + '0') + '0') / 10 + '0';
			write(1, &d, 1);
			d = ((c1 - 1) % 10 + '0');
			write(1, &d, 1);
			write(1, " ", 1);
			d = (c2 - (c2 % 10 + '0') + '0') / 10 + '0';
			write(1, &d, 1);
			d = (c2 % 10 + '0');
			write(1, &d, 1);
			write(1, ", ", 2);
			c2++;
		}
	}
	write(1, "98 99", 5);
	return ;
}
