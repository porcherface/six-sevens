#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	t_list		*begin;
	t_list		*tmp;

	begin = (*lst);
	(*lst) = new;
	tmp = begin;
	begin = new;
	while (begin->next)
		begin = begin->next;
	begin->next = tmp;
}
